const Order = require("../../models/Order");
const Cart = require("../../models/Cart");
const Product = require("../../models/Product");

const createOrder = async (req, res) => {
  try {
    const {
      userId,
      cartItems,
      addressInfo,
      orderStatus,
      paymentMethod,
      paymentStatus,
      totalAmount,
      orderDate,
      orderUpdateDate,
      cartId,
    } = req.body;

    // Remove PayPal logic, assume cash payment
    const newlyCreatedOrder = new Order({
      userId,
      cartId,
      cartItems,
      addressInfo,
      orderStatus,
      paymentMethod: "cash", // Payment method is cash
      paymentStatus: "unpaid", // Since it's cash, payment status is "unpaid" initially
      totalAmount,
      orderDate,
      orderUpdateDate,
    });

    // Save the newly created order to the database
    await newlyCreatedOrder.save();

    res.status(201).json({
      success: true,
      message: "Order created successfully with cash payment",
      orderId: newlyCreatedOrder._id,
    });
  } catch (e) {
    console.log(e);
    res.status(500).json({
      success: false,
      message: "Some error occurred!",
    });
  }
};

const capturePayment = async (req, res) => {
  try {
    const { orderId } = req.body;

    let order = await Order.findById(orderId);

    if (!order) {
      return res.status(404).json({
        success: false,
        message: "Order cannot be found",
      });
    }

    // Since it's a cash payment, we directly mark the order as "paid"
    order.paymentStatus = "paid"; // Cash payments are considered paid immediately
    order.orderStatus = "confirmed"; // Confirm the order status

    // Update product stock based on the items in the cart
    for (let item of order.cartItems) {
      let product = await Product.findById(item.productId);

      if (!product) {
        return res.status(404).json({
          success: false,
          message: `Not enough stock for this product ${product.title}`,
        });
      }

      product.totalStock -= item.quantity;
      await product.save();
    }

    // After successful payment, remove the cart
    const getCartId = order.cartId;
    await Cart.findByIdAndDelete(getCartId);

    // Save the updated order
    await order.save();

    res.status(200).json({
      success: true,
      message: "Order confirmed with cash payment",
      data: order,
    });
  } catch (e) {
    console.log(e);
    res.status(500).json({
      success: false,
      message: "Some error occurred!",
    });
  }
};

const getAllOrdersByUser = async (req, res) => {
  try {
    const { userId } = req.params;

    const orders = await Order.find({ userId });

    if (!orders.length) {
      return res.status(404).json({
        success: false,
        message: "No orders found!",
      });
    }

    res.status(200).json({
      success: true,
      data: orders,
    });
  } catch (e) {
    console.log(e);
    res.status(500).json({
      success: false,
      message: "Some error occurred!",
    });
  }
};

